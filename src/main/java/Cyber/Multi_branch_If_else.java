package Cyber;

public class Multi_branch_If_else {
    public static void main(String[] args) {
        char grade = 'D';

        if (grade == 'A') {
            System.out.println("Excellent");
        } else if (grade == 'B') {
            System.out.println("Good");
        } else if (grade == 'C') {
            System.out.println("Not Bad");
        } else {
            System.out.println("Could be Better");
        }
    }
}
