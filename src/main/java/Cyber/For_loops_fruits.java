package Cyber;

public class For_loops_fruits {
    public static void main(String[] args) {
        String [] fruits = new String[3];
        fruits [0] = "apples";
        fruits [1] = "oranges";
        fruits [2] = "pears";

        for (int i = 0; i < 3; i++) {
            System.out.println(fruits [i]);
        }
    }
}
